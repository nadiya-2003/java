import java.util.Scanner;
class EvenOrOdd
{
public static void main(String args[])
{
Scanner s1=new Scanner(System.in);
int c=s1.nextInt();
switch(c)
{
case 1:
System.out.println("1:odd number");
break;

case 2:
System.out.println("2:even number");
break;

case 3:
System.out.println("3:odd number");
break;

case 4:
System.out.println("4:even number");
break;

case 5:
System.out.println("5:odd number");
break;
case 6:
System.out.println("6:even number");
break;

case 7:
System.out.println("7:odd number");
break;

case 8:
System.out.println("8:even number");
break;

case 9:
System.out.println("9:odd number");
break;

case 10:
System.out.println("10:even number");
break;

default:
System.out.println("Invalid");
}
}
}